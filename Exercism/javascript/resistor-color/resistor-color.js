export const colorCode = (color) => {
  switch(color){
    case "Black":
      return 0;
      break;
    case 'Brown':
      return 1;
      break;
    case 'Red':
      return 2;
      break;
  };
};

export const COLORS = ['Black','Brown','Red'];
